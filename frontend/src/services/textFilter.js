// 일본어 히라가나 + 가타카나
const JAPANESE_RE = /[\u3040-\u309F\u30A0-\u30FF\uFF65-\uFF9F]+/g;

// 전각 영문·숫자 → 반각
const FULLWIDTH_RE = /[\uFF01-\uFF5E]/g;

// 의원 영문명 → 한국어
const MEMBER_REPLACE = [
  [/\bGemini\b/gi,     "제미나이"],
  [/\bChatGPT\b/gi,    "챗지피티"],
  [/\bPerplexity\b/gi, "퍼플렉시티"],
  [/\bGrok\b/gi,       "그록"],
  [/\bManus\b/gi,      "마누스"],
  [/\bClaude\b/gi,     "클로드"],
  [/\bAnthropic\b/gi,  "앤스로픽"],
  [/\bOpenAI\b/gi,     "오픈에이아이"],
  [/\bGoogle\b/gi,     "구글"],
  [/\bDeepMind\b/gi,   "딥마인드"],
  [/\bxAI\b/gi,        "엑스에이아이"],
  [/\bMonica\b/gi,     "모니카"],
];

// 괄호 안 영문 제거: (AI regulation) → 제거
const PAREN_EN_RE = /\([A-Za-z][A-Za-z0-9\s\-_,\.]+\)/g;

// 한글 사이에 낀 단독 영문 단어 제거
// 예: "인공지능 regulation은" → "인공지능 은"
const INLINE_EN_RE = /([\uAC00-\uD7A3])\s+[A-Za-z]{2,}(\s+[\uAC00-\uD7A3])/g;

export function filterSpeech(text) {
  if (!text) return text;
  let r = text;

  // 1. 한자 변환
  for (const [h, k] of HANJA_MAP) {
    if (r.includes(h)) r = r.split(h).join(k);
  }

  // 2. 일본어 제거
  r = r.replace(JAPANESE_RE, "");

  // 3. 전각 → 반각
  r = r.replace(FULLWIDTH_RE, (c) => String.fromCharCode(c.charCodeAt(0) - 0xFEE0));

  // 4. 의원 영문명 → 한국어
  for (const [re, ko] of MEMBER_REPLACE) {
    r = r.replace(re, ko);
  }

  // 5. 괄호 안 영문 제거
  r = r.replace(PAREN_EN_RE, "");

  // 6. 한글 사이 낀 영문 제거
  r = r.replace(INLINE_EN_RE, "$1$2");

  // 7. 연속 공백 정리
  r = r.replace(/\s{2,}/g, " ").trim();

  return r;
}
